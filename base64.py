def main():
    parser = argparse.ArgumentParser(description='Base64 编解码工具')
    parser.add_argument('-e', '--encode', type=str, help='待编码的字符串')
    parser.add_argument('-d', '--decode', type=str, help='待解码的 Base64 字符串')

    args = parser.parse_args()

    if args.encode:
        print('编码结果:', encode(args.encode))
    elif args.decode:
        print('解码结果:', decode(args.decode))
    else:
        print('请提供需要编码或解码的字符串。')

