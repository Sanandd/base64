import argparse
import textwrap
import base64

def encode(data):
    encoded_bytes = base64.b64encode(data.encode('utf-8'))
    return encoded_bytes.decode('utf-8')

def decode(data):
    decoded_bytes = base64.b64decode(data)
    return decoded_bytes.decode('utf-8')
